"""Basis forecast labels.

For horizon h in {1m, 5m, 15m, 1h}:
    future_basis_bps(t, h) = cross_quote_basis_bps(t + h)

Tasks:
    regression: predict future basis bps
    classification: predict abs(future_basis_bps) > threshold

Thresholds: 5, 10, 25, 50 bps

IMPORTANT: Labels are generated by shifting the basis column forward in time.
No future information is used at feature time t.
"""

from __future__ import annotations

import polars as pl

from stressbench.common.logging import get_logger

logger = get_logger(__name__)

_HORIZONS_NS = {
    "1m": 60_000_000_000,
    "5m": 300_000_000_000,
    "15m": 900_000_000_000,
    "1h": 3_600_000_000_000,
}
_THRESHOLDS_BPS = [5.0, 10.0, 25.0, 50.0]


def add_basis_labels(
    df: pl.DataFrame,
    basis_col: str = "cross_quote_basis_bps",
    ts_col: str = "ts_ns",
) -> pl.DataFrame:
    """Add forward-looking basis labels to a feature DataFrame.

    The DataFrame must be sorted by ``ts_col`` ascending and must contain
    one row per timestamp per instrument.

    Args:
        df: Feature DataFrame with ``ts_col`` and ``basis_col`` columns.
        basis_col: Name of the cross-quote basis column.
        ts_col: Name of the timestamp column (nanoseconds).

    Returns:
        DataFrame with additional label columns appended.
    """
    if df.is_empty() or basis_col not in df.columns:
        return df

    df = df.sort(ts_col)

    for horizon_name, horizon_ns in _HORIZONS_NS.items():
        # Shift: future basis = basis at t + horizon_ns.
        # Build a lookup frame keyed at (t + horizon_ns) → shifted back to t,
        # then join with join_asof (nearest, 90 s tolerance) to handle irregular
        # or gapped data where t + horizon_ns may not have an exact match.
        future = df.select(
            [
                (pl.col(ts_col) - horizon_ns).alias(ts_col),
                pl.col(basis_col).alias(f"label_basis_{horizon_name}"),
            ]
        ).sort(ts_col)
        df = df.join_asof(
            future,
            on=ts_col,
            strategy="nearest",
            tolerance=90_000_000_000,  # 90 s in nanoseconds
        )

        # Classification labels
        for threshold in _THRESHOLDS_BPS:
            col_name = f"label_basis_{horizon_name}_gt{int(threshold)}bps"
            df = df.with_columns(
                (
                    pl.col(f"label_basis_{horizon_name}").abs() > threshold
                ).cast(pl.Int8).alias(col_name)
            )

    return df
